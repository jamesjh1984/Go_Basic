package main

import "fmt"

func main() {

	// 转义字符 \t
	fmt.Println("James\tJin")

	// 转义字符 \n
	fmt.Println("James\nJin")

	// 转义字符 \\
	fmt.Println("James\\Jin")

	// 转义字符 \"
	fmt.Println("James\"Jin")

	// 转义字符 \r
	fmt.Println("James\rJin")




	fmt.Println("Name\tAge\tGender\tAddress\nJames\t20\tMale\tHangzhou")
}




